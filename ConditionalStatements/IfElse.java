package ConditionalStatements;

public class IfElse {
    public static void main(String[] args) {
        int time = 11;

        if (time == 12){
            System.out.println("Good day");
        }
        else if (time <12){
            System.out.println("Morning ...");
        }
        else{
            System.out.println("evening....");
        }

    }
}